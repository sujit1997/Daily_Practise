package R_7_3;

public class multiDimensionalArray {
    public static void main(String[] args) {
        String [][] names ={
                {"Mr.", "Mrs.", "Ms."},
                {"Smith", "Jones"},
                {"anesh", "barange"}
        };
        System.out.println(names[0][0]+names[1][0]);
        System.out.println(names[0][1]+names[1][1]);
        System.out.println(names.length);
       
    }
}
